using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;

namespace AtomUI.Generator;

internal class TokenValueConverterRegistryClassWriter
{
    private readonly SourceProductionContext _context;
    private readonly IReadOnlyList<string> _converterTypes;

    public TokenValueConverterRegistryClassWriter(SourceProductionContext context, IEnumerable<string> converterTypes)
    {
        _context        = context;
        _converterTypes = converterTypes.OrderBy(typeName => typeName).ToList();
    }

    public void Write()
    {
        var sourceText = SourceText.From(BuildSource(), Encoding.UTF8);
        _context.AddSource("TokenValueConverterRegistry.g.cs", sourceText);
    }

    private string BuildSource()
    {
        var source = new StringBuilder();
        source.AppendLine("// <auto-generated />");
        source.AppendLine("using System;");
        source.AppendLine("using System.Collections.Generic;");
        source.AppendLine();
        source.AppendLine("namespace AtomUI.Theme.TokenSystem;");
        source.AppendLine();
        source.AppendLine("internal static class TokenValueConverterRegistry");
        source.AppendLine("{");
        source.AppendLine("    public static Dictionary<Type, ITokenValueConverter> Create()");
        source.AppendLine("    {");
        source.AppendLine("        ITokenValueConverter[] valueConverters = new ITokenValueConverter[]");
        source.AppendLine("        {");

        foreach (var converterType in _converterTypes)
        {
            source.Append("            new ");
            source.Append(converterType);
            source.AppendLine("(),");
        }

        source.AppendLine("        };");
        source.AppendLine();
        source.AppendLine("        var map = new Dictionary<Type, ITokenValueConverter>(valueConverters.Length);");
        source.AppendLine("        foreach (var valueConverter in valueConverters)");
        source.AppendLine("        {");
        source.AppendLine("            map.Add(valueConverter.TargetType(), valueConverter);");
        source.AppendLine("        }");
        source.AppendLine();
        source.AppendLine("        return map;");
        source.AppendLine("    }");
        source.AppendLine("}");
        return source.ToString();
    }
}
