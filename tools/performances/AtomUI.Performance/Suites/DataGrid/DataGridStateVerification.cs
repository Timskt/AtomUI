    private static void VerifyDataGridFilterItemChildrenStayLazy(ICollection<string> failures)
    {
        var childrenField = typeof(DataGridFilterItem).GetField(
            "_children",
            BindingFlags.Instance | BindingFlags.NonPublic);
        var hasChildrenProperty = typeof(DataGridFilterItem).GetProperty(
            "HasChildren",
            BindingFlags.Instance | BindingFlags.NonPublic);
        Expect(childrenField != null && hasChildrenProperty != null,
            "DataGridFilterItem should expose lazy children backing members for allocation verification.",
            failures);
        if (childrenField == null || hasChildrenProperty == null)
        {
            return;
        }

        var leaf = new DataGridFilterItem { Text = "Leaf", Value = "Leaf" };
        Expect(childrenField.GetValue(leaf) == null,
            "DataGridFilterItem leaf should not allocate an empty Children list on construction.",
            failures);
        Expect(hasChildrenProperty.GetValue(leaf) is false,
            "DataGridFilterItem HasChildren should report false for a leaf item.",
            failures);
        Expect(childrenField.GetValue(leaf) == null,
            "DataGridFilterItem HasChildren should not materialize the Children list for a leaf item.",
            failures);

        var children = leaf.Children;
        Expect(children.Count == 0 && ReferenceEquals(childrenField.GetValue(leaf), children),
            "DataGridFilterItem public Children getter should still return a mutable empty list when requested.",
            failures);

        VerifyDataGridFilterItemChildrenStayLazy(
            DataGridFilterMode.Menu,
            childrenField,
            hasChildrenProperty,
            failures);
        VerifyDataGridFilterItemChildrenStayLazy(
            DataGridFilterMode.Tree,
            childrenField,
            hasChildrenProperty,
            failures);
    }

