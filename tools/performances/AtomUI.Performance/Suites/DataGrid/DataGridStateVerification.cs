    private static void VerifyDataGridFilterFlyoutPresenterSelectedValuesPreallocateCapacity(
        DataGridFilterMode filterMode,
        string presenterTypeName,
        ICollection<string> failures)
    {
        var grid = CreateFilterCapacityDataGrid(filterMode);
        using var realizedGrid = RealizeControl(grid);

        var indicator = GetDataGridFilterIndicators(grid)
            .FirstOrDefault(indicator => GetDataGridFilterFlyout(indicator) != null);
        Expect(indicator != null,
            $"DataGrid {filterMode} selected-values capacity verification should realize a filter indicator with a flyout.",
            failures);
        if (indicator == null)
        {
            return;
        }

        MaterializeDataGridFilterFlyoutContent(indicator);
        var flyout = GetDataGridFilterFlyout(indicator);
        Expect(flyout != null,
            $"DataGrid {filterMode} selected-values capacity verification should expose a filter flyout.",
            failures);
        if (flyout == null)
        {
            return;
        }

        var presenter = CreateDataGridFilterFlyoutPresenter(flyout);
        Expect(presenter is ItemsControl && presenter.GetType().Name == presenterTypeName,
            $"DataGrid {filterMode} selected-values capacity verification should create {presenterTypeName}. Actual: {presenter?.GetType().Name ?? "null"}.",
            failures);
        if (presenter is not ItemsControl itemsPresenter)
        {
            return;
        }

        var unrealizedEmptyValues = InvokeDataGridFilterPresenterGetValues(presenter, failures);
        Expect(unrealizedEmptyValues != null && unrealizedEmptyValues.Count == 0 && unrealizedEmptyValues.Capacity == 0,
            $"DataGrid {filterMode} unrealized filter presenter should keep empty selected-values list allocation-free. Actual count/capacity: {unrealizedEmptyValues?.Count}/{unrealizedEmptyValues?.Capacity}.",
            failures);

        var unrealizedValueLeaves = GetDataGridFilterPresenterValueLeaves(itemsPresenter);
        Expect(unrealizedValueLeaves.Count == 6,
            $"DataGrid {filterMode} unrealized filter presenter should resolve 6 nested filter value leaves through ItemsView fallback. Actual: {unrealizedValueLeaves.Count}.",
            failures);
        if (unrealizedValueLeaves.Count > 0)
        {
            SetDataGridFilterPresenterValueLeaf(unrealizedValueLeaves[0], true);
            var unrealizedSingleSelectedValues = InvokeDataGridFilterPresenterGetValues(presenter, failures);
            Expect(unrealizedSingleSelectedValues != null && unrealizedSingleSelectedValues.Count == 1,
                $"DataGrid {filterMode} unrealized single checked filter presenter should report one selected value. Actual: {unrealizedSingleSelectedValues?.Count}.",
                failures);
            Expect(unrealizedSingleSelectedValues != null && unrealizedSingleSelectedValues.Capacity == 1,
                $"DataGrid {filterMode} unrealized single selected-values list should preallocate exactly to selected value count. Actual count/capacity: {unrealizedSingleSelectedValues?.Count}/{unrealizedSingleSelectedValues?.Capacity}.",
                failures);

            InvokeDataGridFilterPresenterResetFilter(presenter, failures);
            var unrealizedResetValues = InvokeDataGridFilterPresenterGetValues(presenter, failures);
            Expect(unrealizedResetValues != null && unrealizedResetValues.Count == 0,
                $"DataGrid {filterMode} unrealized filter presenter reset should clear checked nested values through ItemsView fallback. Actual: {unrealizedResetValues?.Count}.",
                failures);
        }

        using var realizedPresenter = RealizeControl(presenter);

        var emptyValues = InvokeDataGridFilterPresenterGetValues(presenter, failures);
        Expect(emptyValues != null && emptyValues.Count == 0 && emptyValues.Capacity == 0,
            $"DataGrid {filterMode} filter presenter should keep empty selected-values list allocation-free. Actual count/capacity: {emptyValues?.Count}/{emptyValues?.Capacity}.",
            failures);

        var valueLeaves = GetDataGridFilterPresenterValueLeaves(itemsPresenter);
        Expect(valueLeaves.Count == 6,
            $"DataGrid {filterMode} filter presenter should expose 6 nested filter value leaves for capacity verification. Actual: {valueLeaves.Count}.",
            failures);

        if (valueLeaves.Count == 0)
        {
            return;
        }

        CheckDataGridFilterPresenterValueLeaf(valueLeaves[0]);
        var singleSelectedValues = InvokeDataGridFilterPresenterGetValues(presenter, failures);
        Expect(singleSelectedValues != null && singleSelectedValues.Count == 1,
            $"DataGrid {filterMode} single checked filter presenter should report one selected value. Actual: {singleSelectedValues?.Count}.",
            failures);
        Expect(singleSelectedValues != null && singleSelectedValues.Capacity == 1,
            $"DataGrid {filterMode} single selected-values list should preallocate exactly to selected value count. Actual count/capacity: {singleSelectedValues?.Count}/{singleSelectedValues?.Capacity}.",
            failures);

        for (var i = 1; i < valueLeaves.Count; i++)
        {
            CheckDataGridFilterPresenterValueLeaf(valueLeaves[i]);
        }

        var selectedValues = InvokeDataGridFilterPresenterGetValues(presenter, failures);
        Expect(selectedValues != null && selectedValues.Count == valueLeaves.Count,
            $"DataGrid {filterMode} checked filter presenter should report all checked selected values. Actual: {selectedValues?.Count}.",
            failures);
        Expect(selectedValues != null && selectedValues.Capacity == valueLeaves.Count,
            $"DataGrid {filterMode} selected-values list should preallocate to leaf value count after first selected value. Actual count/capacity/leaves: {selectedValues?.Count}/{selectedValues?.Capacity}/{valueLeaves.Count}.",
            failures);
    }

