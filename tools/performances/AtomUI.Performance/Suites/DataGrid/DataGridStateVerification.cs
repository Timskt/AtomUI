    private static void VerifyDataGridFilterFlyoutPassiveCloseSkipsUnusedSelection(
        DataGridFilterMode filterMode,
        string presenterTypeName,
        ICollection<string> failures)
    {
        var grid = CreateFilterDataGrid(filterMode);
        using var realizedGrid = RealizeControl(grid);

        var indicator = GetDataGridFilterIndicators(grid)
            .FirstOrDefault(indicator => GetDataGridFilterFlyout(indicator) != null);
        Expect(indicator != null,
            $"DataGrid {filterMode} filter close verification should realize a filter indicator with a flyout.",
            failures);
        if (indicator == null)
        {
            return;
        }

        MaterializeDataGridFilterFlyoutContent(indicator);
        var flyout = GetDataGridFilterFlyout(indicator);
        Expect(flyout != null,
            $"DataGrid {filterMode} filter close verification should expose a filter flyout.",
            failures);
        if (flyout == null)
        {
            return;
        }

        var presenter = CreateDataGridFilterFlyoutPresenter(flyout);
        Expect(presenter != null && presenter.GetType().Name == presenterTypeName,
            $"DataGrid {filterMode} filter close verification should create {presenterTypeName}. Actual: {presenter?.GetType().Name ?? "null"}.",
            failures);
        if (presenter == null)
        {
            return;
        }

        var column = grid.Columns[0];
        flyout.Popup.Child = presenter;
        var skippedArgs = InvokeDataGridFilterFlyoutClosedAndCaptureSelection(flyout, failures);
        Expect(skippedArgs == null,
            $"DataGrid {filterMode} default passive filter close should not collect or report selected values when FilterOnClose=false.",
            failures);

        column.FilterOnClose = true;
        var filterOnCloseArgs = InvokeDataGridFilterFlyoutClosedAndCaptureSelection(flyout, failures);
        var filterOnCloseValues = GetDataGridFilterSelectedValues(filterOnCloseArgs);
        Expect(filterOnCloseValues.Count == 0,
            $"DataGrid {filterMode} FilterOnClose passive close without checked containers should still report an empty selected value list. Actual: {string.Join(", ", filterOnCloseValues)}.",
            failures);
        Expect(GetDataGridFilterValuesSelectedIsConfirmed(filterOnCloseArgs) == false,
            $"DataGrid {filterMode} FilterOnClose passive close should preserve inactive shutdown state.",
            failures);

        column.FilterOnClose = false;
        SetDataGridFilterFlyoutActiveShutdown(flyout, true, failures);
        var activeCloseArgs = InvokeDataGridFilterFlyoutClosedAndCaptureSelection(flyout, failures);
        var activeCloseValues = GetDataGridFilterSelectedValues(activeCloseArgs);
        Expect(activeCloseValues.Count == 0,
            $"DataGrid {filterMode} active filter close without checked containers should still report an empty selected value list. Actual: {string.Join(", ", activeCloseValues)}.",
            failures);
        Expect(GetDataGridFilterValuesSelectedIsConfirmed(activeCloseArgs) == true,
            $"DataGrid {filterMode} active close should preserve confirmed shutdown state.",
            failures);
        flyout.Popup.Child = null;
    }

