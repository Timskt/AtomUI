    private static object? InvokeDataGridFilterFlyoutClosedAndCaptureSelection(
        PopupFlyoutBase flyout,
        ICollection<string> failures)
    {
        var eventInfo = flyout.GetType()
                              .GetEvent("FilterValuesSelected", BindingFlags.Instance | BindingFlags.Public);
        var handlerType = eventInfo?.EventHandlerType;
        var argsType = handlerType?.GenericTypeArguments.FirstOrDefault();
        var captureMethod = argsType == null
            ? null
            : typeof(Program).GetMethod(
                    nameof(CaptureDataGridFilterValuesSelected),
                    BindingFlags.Static | BindingFlags.NonPublic)
                ?.MakeGenericMethod(argsType);
        var onClosedMethod = flyout.GetType()
                                   .GetMethod("OnClosed", BindingFlags.Instance | BindingFlags.NonPublic);
        Expect(eventInfo != null && handlerType != null && captureMethod != null && onClosedMethod != null,
            $"DataGrid {flyout.GetType().Name} should expose close notification members for selected value verification.",
            failures);
        if (eventInfo == null || handlerType == null || captureMethod == null || onClosedMethod == null)
        {
            return null;
        }

        var handler = Delegate.CreateDelegate(handlerType, captureMethod);
        _capturedDataGridFilterValuesSelectedArgs = null;
        eventInfo.AddEventHandler(flyout, handler);
        try
        {
            onClosedMethod.Invoke(flyout, null);
            return _capturedDataGridFilterValuesSelectedArgs;
        }
        finally
        {
            eventInfo.RemoveEventHandler(flyout, handler);
            _capturedDataGridFilterValuesSelectedArgs = null;
        }
    }

